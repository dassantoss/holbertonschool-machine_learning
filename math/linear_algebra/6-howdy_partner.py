#!/usr/bin/env python3
"""
Function that concatenates two arrays.
"""


def cat_arrays(arr1, arr2):
    """
    Concatenates two lists of integers or floats.

    Parameters:
    arr1 (list of ints/floats): First list to concatenate.
    arr2 (list of ints/floats): Second list to concatenate.

    Returns:
    list: A new list resulting from the concatenation of arr1 and arr2.
    """

    return arr1 + arr2
